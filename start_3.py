# Comment afficher un message dans la console en plus de la valeur d'une variable

nom = "Mohamed"
print(f"Cet homme s'appelle {nom}")

# Lorsque vous ouvrez les parenthèses après print il suffit d'ajouter f" puis d'écrire 
# entre les guillemets votre message, puis vous y ajouterez entre accolade 
# le nom de la variable que vous voulez afficher, ici en l'occurence, 
# nous voulons afficher la valeur de la variable nom, donc nous mettons nom entre accolade
